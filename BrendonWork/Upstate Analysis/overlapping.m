function answer=overlapping (pair1,pair2);
%inputs are each 1x2 pairs, this will give a 1 if they overlap 2 if they do
%not
if ((pair1(1,1)>=pair2(1,1) && pair1(1,1)<=pair2(1,2)) || ... 
    (pair1(1,2)>=pair2(1,1) && pair1(1,2)<=pair2(1,2)) || ...
    (pair2(1,1)>=pair1(1,1) && pair2(1,1)<=pair1(1,2)) || ...
    (pair2(1,2)>=pair1(1,1) && pair2(1,2)<=pair1(1,2)) || ...
    (pair1(1,1)<=pair2(1,1) && pair1(1,2)>=pair2(1,2)) || ...
    (pair1(1,1)>=pair2(1,1) && pair1(1,2)<=pair2(1,2)));
    answer = 1;
else
    answer = 0;
end